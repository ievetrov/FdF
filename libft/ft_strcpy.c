#include "libft.h"

char	*ft_strcpy(char *destination, const char *source)
{
	size_t			i;
	char			*dest;
	const char		*src;

	i = 0;
	src = (const char *)source;
	dest = (char *)destination;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
