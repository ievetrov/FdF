#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*copy_bzero;

	i = 0;
	copy_bzero = (char *)s;
	while (i < n)
	{
		copy_bzero[i] = '\0';
		i++;
	}
}
