int		ft_countallsplit(char const *s, char c)
{
	int i;
	int res;

	i = 0;
	res = 0;
	while (s[i])
	{
		while (s[i] == c)
			i++;
		if (s[i] != c && s[i] != '\0')
			res++;
		while (s[i] != c && s[i] != '\0')
			i++;
	}
	return (res);
}
